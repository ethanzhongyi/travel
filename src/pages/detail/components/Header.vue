<template>
  <div>
    <router-link 
      tag='div' to='/' 
      class='header-abs'
      v-show='showAbs'
    >
      <div class='iconfont abs-back'>&#xe624;</div>
    </router-link>
    <div class='header-fixed' 
      v-show='!showAbs'
      :style='opacityStyle'>
      <router-link to='/'>
  	  <div class='iconfont fixed-back'>&#xe624;</div>
  	  </router-link>
      景点详情
    </div>
  </div>
</template>

<script>
export default {
  name: 'DetailHeader',
  data () {
    return {
      showAbs: true,
      opacityStyle: {
        opacity: 0
      }
    }
  },
  methods: {
    handleScroll () {
      const top = document.documentElement.scrollTop
      if (top > 50) {
        let opacity = top / 140
        opacity = opacity > 1 ? 1 : opacity
        this.opacityStyle = {
          opacity: opacity
        }
        this.showAbs = false
      } else {
        this.showAbs = true
      }
    }
  },
  activated () {
    window.addEventListener('scroll',this.handleScroll)
  },
  deactivated () {
    window.removeEventListener('scroll',this.handleScroll)
  }
}
</script>

